def main() -> None:
  # Path to the log file generated by the ingestion layer
  # In K8s, this might be a shared volume or fetched from Cloud Logging
  LOG_FILE = os.getenv("INGESTION_LOG_FILE", "artifacts/logs/ingestion_latest.log")

  logger.info(f"Checking quality gates in: {LOG_FILE}")

  passed = check_quality_gates(LOG_FILE)

  if not passed:
    send_alert("Quality Gates FAILED. Immediate investigation required.")
    sys.exit(1)

  sys.exit(0)


if __name__ == "__main__":
  main()
