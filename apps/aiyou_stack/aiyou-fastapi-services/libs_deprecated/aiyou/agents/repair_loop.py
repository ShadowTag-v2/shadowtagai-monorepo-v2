import json
import os
import subprocess

from google import genai
from google.genai import types


class SovereignRepairLoop:
    def __init__(self, model_name="gemini-2.0-flash-exp"):
        self.client = genai.Client(vertexai=True, location="us-central1")
        self.model = model_name

    def fixer_agent(self, error: str, code: str, feedback: str = "") -> str:
        """Fixer Agent: Proposes code fixes, learning from feedback."""
        prompt = f"""You are an expert code fixer.
        Given the following code and error, provide only the complete, corrected code block.
        Do not add any explanation/markdown. Just the code.

        Error: {error}

        {f"PREVIOUS FEEDBACK: {feedback}" if feedback else ""}

        Code:
        {code}
        """

        response = self.client.models.generate_content(model=self.model, contents=prompt)
        # Strip markdown code blocks if present
        text = response.text.replace("```python", "").replace("```", "").strip()
        return text

    def reviewer_agent(self, original: str, fix: str, error: str) -> dict:
        """Reviewer Agent: Critiques the fix for correctness and safety."""
        prompt = f"""You are a senior software engineer. Analyze this fix.
        Respond in JSON format with two keys: 'approved' (boolean) and 'reason' (string).

        Original Error: {error}

        Proposed Fix:
        {fix}
        """

        response = self.client.models.generate_content(
            model=self.model,
            contents=prompt,
            config=types.GenerateContentConfig(response_mime_type="application/json"),
        )
        try:
            return json.loads(response.text)
        except json.JSONDecodeError:
            return {"approved": False, "reason": "Reviewer failed to produce valid JSON."}

    def reporter_agent(self, fix: str, file_path: str, error: str) -> str:
        """Reporter Agent: Opens a PR using GitHub CLI."""
        branch_name = f"autofix/error-{os.urandom(4).hex()}"
        pr_title = f"fix: Automated repair for error in {os.path.basename(file_path)}"
        pr_body = f"Automated fix for error:\n`{error}`\n\nGenerated by Sovereign Repair Loop."

        try:
            # 1. Create Branch
            subprocess.run(["git", "checkout", "-b", branch_name], check=True)

            # 2. Apply Fix
            with open(file_path, "w") as f:
                f.write(fix)

            # 3. Commit and Push
            subprocess.run(["git", "add", file_path], check=True)
            subprocess.run(["git", "commit", "-m", pr_title], check=True)
            subprocess.run(["git", "push", "-u", "origin", branch_name], check=True)

            # 4. Create PR
            subprocess.run(
                ["gh", "pr", "create", "--title", pr_title, "--body", pr_body], check=True,
            )

            return f"PR Created: {branch_name}"
        except subprocess.CalledProcessError as e:
            return f"Reporter Failed: {e!s}"
        finally:
            # Cleanup: return to main
            subprocess.run(["git", "checkout", "main"], check=False)

    def execute_loop(self, error: str, code: str, file_path: str, max_retries=3) -> dict:
        """Orchestrates the Fixer-Reviewer loop."""
        feedback = ""

        for attempt in range(max_retries):
            print(f"🔄 Repair Attempt {attempt + 1}/{max_retries}")

            # 1. Fix
            fix = self.fixer_agent(error, code, feedback)
            if not fix:
                feedback = "Fixer produced empty output."
                continue

            # 2. Review
            review = self.reviewer_agent(code, fix, error)

            if review.get("approved"):
                # 3. Report
                report = self.reporter_agent(fix, file_path, error)
                return {"status": "SUCCESS", "fix": fix, "report": report}
            feedback = review.get("reason", "Unknown rejection")
            print(f"❌ Rejected: {feedback}")

        return {"status": "FAILED", "reason": "Max retries exceeded"}
